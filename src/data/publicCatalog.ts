/** Returns the CategoryNode for a given category name, or undefined. */
export function getCategoryNode(name: string): CategoryNode | undefined {
  return CATEGORY_TREE.find(c => c.name === name)
}

/** Returns the SubcategoryCard for a given category name + subcategory slug, or undefined. */
export function getSubcategoryNode(categoryName: string, subcategorySlug: string) {
  return getCategoryNode(categoryName)?.children?.find(c => c.slug === subcategorySlug)
}

/** Returns catalog items matching both a category name and a public subcategory slug. */
export function getCatalogBySubcategory(categoryName: string, subcategorySlug: string): CatalogItem[] {
  return CATALOG_ITEMS.filter(i => i.category === categoryName && i.subcategory === subcategorySlug)
}

